package com.github.mattinfern0.todoaholic.server.users.dtos;

public class UserDto {
    Long id;
    String email;

    public UserDto() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
